package com.lti.type;

public class MPT {
	private Double alpha;
	private Double beta;
	private Double AR;
	private Double RSquared;
	private Double sharpeRatio;
	private Double standardDeviation;
	private Double treynorRatio;
	private Double drawDown;
	private Integer year;
	private java.util.Date startDate;
	private java.util.Date endDate;
	public Double getAlpha() {
		return alpha;
	}
	public void setAlpha(Double alpha) {
		this.alpha = alpha;
	}
	public Double getBeta() {
		return beta;
	}
	public void setBeta(Double beta) {
		this.beta = beta;
	}
	public Double getAR() {
		return AR;
	}
	public void setAR(Double ar) {
		AR = ar;
	}
	public Double getRSquared() {
		return RSquared;
	}
	public void setRSquared(Double squared) {
		RSquared = squared;
	}
	public Double getSharpeRatio() {
		return sharpeRatio;
	}
	public void setSharpeRatio(Double sharpeRatio) {
		this.sharpeRatio = sharpeRatio;
	}
	public Double getStandardDeviation() {
		return standardDeviation;
	}
	public void setStandardDeviation(Double standardDeviation) {
		this.standardDeviation = standardDeviation;
	}
	public Double getTreynorRatio() {
		return treynorRatio;
	}
	public void setTreynorRatio(Double treynorRatio) {
		this.treynorRatio = treynorRatio;
	}
	public Double getDrawDown() {
		return drawDown;
	}
	public void setDrawDown(Double drawDown) {
		this.drawDown = drawDown;
	}
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}
	public java.util.Date getStartDate() {
		return startDate;
	}
	public void setStartDate(java.util.Date startDate) {
		this.startDate = startDate;
	}
	public java.util.Date getEndDate() {
		return endDate;
	}
	public void setEndDate(java.util.Date endDate) {
		this.endDate = endDate;
	}
}
